package basic.programs.java;

public class Arithmatic2Numbers {

	public static void main(String[] args) {
		
		
		
		
		// Widening concept 
		float a = 12;           //lower 
		double b = a; // 12.0   //higher 
		System.out.println(b);
		
		// Arithmetic 
		int x= 23, Y= 45;
		int sum = x + Y ; //23 + 45 = 68
		int mul  = x * Y; //23 * 45 = 1035
		int div = x/Y; // 23 / 45 = 0
		int mod = x%Y; // 23 % 45 = 23
		int sub = x-Y; // 23 - 45 = -22
		System.out.println(sum);
		System.out.println(mul);
		System.out.println(div);
		System.out.println(mod);
		System.out.println(sub);
		
		

	}

}
